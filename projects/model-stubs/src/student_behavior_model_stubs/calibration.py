def _display_metadata(dimension: str) -> dict[str, object]:
    rules = METRIC_RULE_DECLARATIONS[dimension]
    return {
        "dimension_label": DIMENSION_LABELS[dimension],
        "metric_labels": [rule["display_name"] for rule in rules],
        "metric_names": [rule["metric"] for rule in rules],
        "metric_evidence_labels": [rule["evidence_label"] for rule in rules],
        "metric_aggregations": [rule["aggregation"] for rule in rules],
        "metric_directions": [rule["influence_direction"] for rule in rules],
        "level_labels": LEVEL_LABELS,
        "threshold_strategies": [rule["threshold_strategy"] for rule in rules],
        "support_metric_labels": [
            rule["display_name"] for rule in rules if rule.get("evidence_role") == "support"
        ],
    }


METRIC_DISPLAY_METADATA = {
    dimension: _display_metadata(dimension) for dimension in CALIBRATED_DIMENSIONS
}
