def _rule(
    dimension_code: str,
    metric: str,
    display_name: str,
    raw_fields: list[str],
    threshold_strategy: str,
    *,
    influence_direction: str,
    aggregation: str,
    evidence_label: str,
    deferred_status: dict[str, object] | None = None,
    caveat: str | None = None,
    evidence_role: str = "primary",
) -> dict[str, object]:
    rule = {
        "dimension_code": dimension_code,
        "dimension_label": DIMENSION_LABELS[dimension_code],
        "metric": metric,
        "display_name": display_name,
        "evidence_label": evidence_label,
        "raw_fields": raw_fields,
        "threshold_strategy": threshold_strategy,
        "influence_direction": influence_direction,
        "aggregation": aggregation,
        "deferred_status": deferred_status or {"state": "none"},
        "evidence_role": evidence_role,
    }
    if caveat is not None:
        rule["caveat"] = caveat
    return rule


